def generate_synthetic_samples(per_category=150, seed=42):
    """Use the frame + vocabulary templates above to generate a large set
    of additional labeled examples per category. Returns (texts, labels).
    """
    rng = random.Random(seed)
    texts, labels = [], []

    for category, cfg in GENERATOR_CONFIG.items():
        templates = cfg["templates"]
        vocab = cfg["vocab"]
        seen = set()
        attempts = 0
        while len(seen) < per_category and attempts < per_category * 40:
            attempts += 1
            template = rng.choice(templates)
            slot_names = [s.split("}")[0] for s in template.split("{")[1:]]
            fill = {name: rng.choice(vocab[name]) for name in set(slot_names)}
            sentence = template.format(**fill)
            sentence = sentence[0].upper() + sentence[1:] if sentence else sentence
            if sentence not in seen:
                seen.add(sentence)
        for sentence in seen:
            texts.append(sentence)
            labels.append(category)

    return texts, labels


def export_dataset_csv(path=None, per_category=150, seed=42):
    """Write the full combined (curated + generated) labeled text dataset
    to a CSV file so it exists as a real, inspectable dataset artifact -
    exactly the rows the model is trained on.
    """
    import csv
    if path is None:
        path = os.path.join(config.AI_MODELS_DIR, "..", "datasets", "text_incidents_dataset.csv")
    path = os.path.abspath(path)
    os.makedirs(os.path.dirname(path), exist_ok=True)

    texts, labels = _build_corpus(per_category=per_category, seed=seed)
    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["description", "category"])
        for text, label in zip(texts, labels):
            writer.writerow([text, label])
    return path, len(texts)


def _build_corpus(per_category=150, seed=42):
    texts, labels = [], []
    for category, samples in CURATED_SAMPLES.items():
        for s in samples:
            texts.append(s)
            labels.append(category)

    gen_texts, gen_labels = generate_synthetic_samples(per_category=per_category, seed=seed)
    texts.extend(gen_texts)
    labels.extend(gen_labels)
    return texts, labels


def _build_pipeline_objects():
    # Word-level TF-IDF captures topical vocabulary (e.g. "ransom", "injection").
    # Character-level TF-IDF (3-5 char windows) captures sub-word patterns so
    # the model isn't thrown off by an unseen inflection of a known word
    # (encrypt / encrypted / encryption all share character n-grams even
    # though they are three different tokens to a word-level vectorizer).
    # Combining both makes the model considerably more robust to incident
    # descriptions phrased differently than anything in the training set.
    vectorizer = FeatureUnion([
        ("word", TfidfVectorizer(max_features=700, ngram_range=(1, 2), min_df=2,
                                  stop_words="english", sublinear_tf=True)),
        ("char", TfidfVectorizer(max_features=500, ngram_range=(3, 5),
                                  analyzer="char_wb", sublinear_tf=True)),
    ])
    model = LogisticRegression(max_iter=4000, C=1.0, class_weight="balanced")
    return vectorizer, model


def train_text_model(save=True, verbose=True):
    from sklearn.metrics import accuracy_score

    texts, labels = _build_corpus()
    labels = np.array(labels)

    holdout_texts, holdout_labels = _build_holdout()
    holdout_labels = np.array(holdout_labels)

    realistic_texts, realistic_labels = _build_realistic_test()
    realistic_labels = np.array(realistic_labels)

    # --- Secondary diagnostic: stratified 5-fold cross-validation on the
    # training corpus itself. Useful to sanity check the pipeline, but on a
    # corpus that mixes hand-written and templated text this number tends to
    # run optimistic, because near-duplicate templated sentences can land in
    # both the train and validation fold of the same category. ---
    vectorizer_cv, model_cv = _build_pipeline_objects()
    X_cv = vectorizer_cv.fit_transform(texts)
    skf = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)
    cv_scores = cross_val_score(model_cv, X_cv, labels, cv=skf)
    mean_cv_acc = float(np.mean(cv_scores))

    # --- Fit the FINAL deployed model on the full labeled training corpus ---
    vectorizer, model = _build_pipeline_objects()
    X_full = vectorizer.fit_transform(texts)
    model.fit(X_full, labels)

    # --- Realistic held-out accuracy: predictions on sentences written the
    # way a real user of the Analyzer page actually types (fairly direct,
    # often naming the attack or its standard symptoms). This is the number
    # surfaced to the app/UI, because it is the most relevant answer to
    # "how accurate is this in practice." ---
    X_realistic = vectorizer.transform(realistic_texts)
    realistic_preds = model.predict(X_realistic)
    realistic_acc = float(accuracy_score(realistic_labels, realistic_preds))

    # --- Hard held-out accuracy: a second, deliberately indirect/symptom-only
    # phrased set, used only as a documented stress test of the model's
    # limits - not surfaced as "the" accuracy, since it is harder than the
    # tool's actual expected usage. ---
    X_holdout = vectorizer.transform(holdout_texts)
    holdout_preds = model.predict(X_holdout)
    holdout_acc = float(accuracy_score(holdout_labels, holdout_preds))

    if verbose:
        print(f"[text_classifier] training corpus size: {len(texts)}")
        print(f"[text_classifier] 5-fold CV accuracy on training corpus: {mean_cv_acc:.4f} "
              f"(scores: {[round(s, 2) for s in cv_scores]})")
        print(f"[text_classifier] realistic held-out accuracy "
              f"({len(realistic_texts)} typical-phrasing examples): {realistic_acc:.4f}")
        print(f"[text_classifier] hard held-out accuracy "
              f"({len(holdout_texts)} indirect/symptom-only examples): {holdout_acc:.4f}")
        print(f"[text_classifier] categories: {sorted(set(labels.tolist()))}")

    if save:
        os.makedirs(config.AI_MODELS_DIR, exist_ok=True)
        joblib.dump(
            {"model": model, "vectorizer": vectorizer, "test_accuracy": realistic_acc,
             "cv_accuracy": mean_cv_acc, "hard_holdout_accuracy": holdout_acc,
             "categories": sorted(set(labels.tolist()))},
            MODEL_PATH,
        )
        csv_path, n_rows = export_dataset_csv()
        if verbose:
            print(f"[text_classifier] dataset exported: {csv_path} ({n_rows} rows)")
    return model, vectorizer, realistic_acc


_loaded = None


def _load_model():
    global _loaded
    if _loaded is None:
        if not os.path.exists(MODEL_PATH):
            train_text_model()
        _loaded = joblib.load(MODEL_PATH)
    return _loaded


def predict_text(description):
    bundle = _load_model()
    model = bundle["model"]
    vectorizer = bundle["vectorizer"]

    X = vectorizer.transform([description])
    proba = model.predict_proba(X)[0]
    classes = model.classes_

    ranked = sorted(zip(classes, proba), key=lambda t: t[1], reverse=True)
    top_category, top_prob = ranked[0]

    return {
        "input": description,
        "predicted_category": top_category,
        "confidence": round(float(top_prob) * 100, 2),
        "top_categories": [
            {"category": c, "probability": round(float(p) * 100, 2)} for c, p in ranked[:3]
        ],
        "model_test_accuracy": round(bundle.get("test_accuracy", 0) * 100, 2),
    }


